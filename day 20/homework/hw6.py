"""6) მომხმარებელმა უნდა შეიყვანოს რიცხვები, სანამ -1-ს არ შეიყვანს. საბოლოოდ გამოიანგარიშოს შეყვანილი რიცხვების საშუალო"""

number = int(input("შეიყვანეთ რიცხვი: "))
total = 0
while number != -1:
    total += 1
    number += number
    number = int(input("შეიყვანეთ რიცხვი: "))
else:
    print(f"შეყვანილი რიცხვების საშუალო არის: {number / total}")